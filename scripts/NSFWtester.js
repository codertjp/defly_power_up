String.prototype.strip = function () {
  const charMapObject = Object.fromEntries(
    charMap.flatMap((chars) =>
      chars.map((char) => [char, chars[chars.length - 1]])
    )
  );
  return this.split("")
    .map((char) => charMapObject[char] || char)
    .join("");
};

