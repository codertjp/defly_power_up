function encryptList(list, shift = 3) {
  return list.map((item) => shiftString(item, shift));
}

function decryptList(encryptedList, shift = 3) {
  return encryptList(encryptedList, -shift); // Decryption is just shifting in the opposite direction
}

function shiftString(str, shift) {
  return str
    .split("")
    .map((char) => shiftCharacter(char, shift))
    .join("");
}

function shiftCharacter(char, shift) {
  const charCode = char.charCodeAt(0);
  const shiftedCharCode = charCode + shift;
  return String.fromCharCode(shiftedCharCode);
}

